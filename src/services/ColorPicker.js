const ColorPicker = {
  getLightColor(index){
    switch(index) {
      case 1:
        return "#9BD0B7";
        break;
      case 2:
        return "#A0CED9";
        break;
      case 3:
        return "#C0C6ED";
        break;
      case 4:
        return "#F1C5AE";
        break;
      case 5:
        return "#DABFDE";
        break;
    }
  }
}

export default ColorPicker;
